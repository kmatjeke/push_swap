#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*dest;
	int		i;
	int		j;

	j = 0;
	while (s1[j])
		j++;
	i = 0;
	if (!s1 || !(dest = (char *)malloc(sizeof(char) * j + 1)))
		return (NULL);
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
